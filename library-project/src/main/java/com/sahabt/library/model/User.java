package com.sahabt.library.model;

public class User {
	
	private String id;
	
	private String name;
	
	private String surname;
	
	private User statu;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSurname() {
		return surname;
	}

	public void setSurname(String surname) {
		this.surname = surname;
	}

	public User getStatu() {
		return statu;
	}

	public void setStatu(User statu) {
		this.statu = statu;
	}
	
	

}
